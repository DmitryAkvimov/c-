# Ввод количества карточек
N = int(input("Введите количество карточек: "))

# Вычисляем сумму всех карточек от 1 до N
total_sum = N * (N + 1) // 2

# Вводим оставшиеся карточки и вычитаем их из общей суммы
for _ in range(N - 1):
    remaining_card = int(input("Введите номер оставшейся карточки: "))
    total_sum -= remaining_card

# Выводим номер потерянной карточки
print(f"Потерянная карточка: {total_sum}")
