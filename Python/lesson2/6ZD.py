# Вводим количество мальчиков и девочек
x = int(input("Введите количество мальчиков: "))
y = int(input("Введите количество девочек: "))

# Проверяем условие возможности корректной рассадки
if x > 2 * y or y > 2 * x:
    print("Нет решения")
else:
    # Если мальчиков больше или их количество равно количеству девочек
    if x >= y:
        # Формируем чередование мальчиков и девочек
        result = ""
        # Пока остаются мальчики и девочки
        while x > 0 or y > 0:
            if x > 0:  # Добавляем мальчика
                result += "B"
                x -= 1
            if y > 0:  # Добавляем девочку
                result += "G"
                y -= 1
            if x > y and x > 0:  # Добавляем лишнего мальчика, если их больше
                result += "B"
                x -= 1
    # Если девочек больше
    else:
        # Формируем чередование девочек и мальчиков
        result = ""
        # Пока остаются девочки и мальчики
        while x > 0 or y > 0:
            if y > 0:  # Добавляем девочку
                result += "G"
                y -= 1
            if x > 0:  # Добавляем мальчика
                result += "B"
                x -= 1
            if y > x and y > 0:  # Добавляем лишнюю девочку, если их больше
                result += "G"
                y -= 1
    # Выводим результат
    print(result)
