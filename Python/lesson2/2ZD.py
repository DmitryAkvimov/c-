# Инициализация переменных
tasks_sum = 0
go_to_store = False

# Запуск цикла на 8 часов
print("Начался восьмичасовой рабочий день.")

for hour in range(1, 9):  # Цикл от 1 до 8 включительно
    print(f"{hour}-й час")
    
    # Запрашиваем количество задач
    tasks = int(input("Сколько задач решит Максим? "))
    tasks_sum += tasks  # Увеличиваем счетчик решённых задач
    
    # Спрашиваем, взял ли Максим трубку
    call = int(input("Звонит жена. Взять трубку? (1 — да, 0 — нет): "))
    if call == 1:
        go_to_store = True  # Если взял трубку, устанавливаем флаг

# Вывод результатов рабочего дня
print(f"Рабочий день закончился. Всего выполнено задач: {tasks_sum}")

# Проверяем, нужно ли зайти в магазин
if go_to_store:
    print("Нужно зайти в магазин.")