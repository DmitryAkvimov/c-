def my_sum(*args):
    total_sum = 0  # Переменная для хранения итоговой суммы
    
    # Проходим по всем аргументам
    for elem in args:
        if isinstance(elem, (int, float)):  # Если элемент — число, добавляем его к сумме
            total_sum += elem
        elif isinstance(elem, (list, tuple)):  # Если элемент — список или кортеж, рекурсивно обрабатываем его
            total_sum += my_sum(*elem)
    
    return total_sum

# Примеры тестирования
print("Сумма элементов [[1, 2, [3]], [1], 3]:", my_sum([[1, 2, [3]], [1], 3]))  # Ожидаемый результат: 10
print("Сумма элементов (1, 2, 3, 4, 5):", my_sum(1, 2, 3, 4, 5))               # Ожидаемый результат: 15
print("Сумма элементов [1, [2, 3]], 4, (5, 6):", my_sum([1, [2, 3]], 4, (5, 6))) # Ожидаемый результат: 21
print("Сумма элементов [1, [2, [3, 4]]], 5:", my_sum([1, [2, [3, 4]]], 5))      # Ожидаемый результат: 15
